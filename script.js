const labelByType = {
  articles: "Article",
  multimedia: "Multimedia",
  social: "Social Media",
};

function sourceLabel(url) {
  if (url.includes("instagram.com")) return "Instagram";
  if (url.includes("issuu.com")) return "Issuu";
  if (url.includes("drive.google.com")) return "Google Drive";
  if (url.includes("filesusr.com")) return "PDF";
  return "The Epitaph";
}

function createPortfolioCard(item, type) {
  const card = document.createElement("article");
  card.className = "portfolio-card";

  const image = document.createElement("img");
  image.src = item.image;
  image.alt = item.title;
  image.loading = "lazy";

  const body = document.createElement("div");
  body.className = "portfolio-card-body";

  const meta = document.createElement("div");
  meta.className = "portfolio-meta";

  const format = document.createElement("span");
  format.textContent = labelByType[type];

  const source = document.createElement("span");
  source.textContent = sourceLabel(item.href);

  const title = document.createElement("h3");
  title.textContent = item.title;

  const description = document.createElement("p");
  description.textContent =
    type === "social" ? "Social media coverage." : type === "multimedia" ? "Multimedia reporting." : "Published work.";

  const link = document.createElement("a");
  link.href = item.href;
  link.target = "_blank";
  link.rel = "noreferrer";
  link.textContent = type === "social" ? "Open post" : "Open work";
  link.setAttribute("aria-label", `Open ${item.title}`);

  meta.append(format, source);
  body.append(meta, title, description, link);
  card.append(image, body);
  return card;
}

function renderPortfolio() {
  Object.entries(portfolioData).forEach(([type, items]) => {
    const panel = document.querySelector(`[data-panel="${type}"]`);
    const grid = document.createElement("div");
    grid.className = "portfolio-grid";

    items.forEach((item) => {
      grid.append(createPortfolioCard(item, type));
    });

    panel.replaceChildren(grid);
  });
}

function activateTab(type) {
  document.querySelectorAll(".portfolio-tab").forEach((tab) => {
    const isActive = tab.dataset.tab === type;
    tab.classList.toggle("active", isActive);
    tab.setAttribute("aria-selected", String(isActive));
  });

  document.querySelectorAll(".portfolio-panel").forEach((panel) => {
    const isActive = panel.dataset.panel === type;
    panel.classList.toggle("active", isActive);
    panel.hidden = !isActive;
  });
}

document.querySelectorAll(".portfolio-tab").forEach((tab) => {
  tab.addEventListener("click", () => activateTab(tab.dataset.tab));
});

